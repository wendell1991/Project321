package com.mathtimeexplorer.main;

public class News {

	private int new_id;
	private String news_title;
	private String news_postdate;
	
	public int getNew_id() {
		return new_id;
	}
	
	public void setNew_id(int new_id) {
		this.new_id = new_id;
	}
	
	public String getNews_title() {
		return news_title;
	}
	
	public void setNews_title(String news_title) {
		this.news_title = news_title;
	}
	
	public String getNews_postdate() {
		return news_postdate;
	}
	
	public void setNews_postdate(String news_postdate) {
		this.news_postdate = news_postdate;
	}
}
